// Variáveis e Tipos

package aulas.a1_variaveis_e_tipos;

public class Sample3 {
    public static void main(String[] args) {
        System.out.print("Digite um número inteiro: ");
        int numeroEscolhido = 319;
        // int: Número inteiro mediano (404)
        // short: Número inteiro pequeno (15)
        // long: Número inteiro grande (1234567890)
        // float: Número decimal padrão (3.14f)
        // double: Número decimal com precisão dupla (3.141592)
        // String: Texto qualquer ("Olá, mundo!")
        // char: Um caractere qualquer ('M');
        // Boolean: Verdadeiro ou Falso (true, false)

        System.out.println("Número escolhido: " + numeroEscolhido);
    }
}
