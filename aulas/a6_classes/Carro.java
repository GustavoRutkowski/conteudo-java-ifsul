package aulas.a6_classes;

public class Carro {
    public String placa;
    public String marca;
    public String modelo;
    public int numeroPortas;
    public String cor;

    public Carro(String placa, String marca, String modelo, int numeroPortas, String cor) {
        this.placa = placa;
        this.marca = marca;
        this.modelo = modelo;
        this.numeroPortas = numeroPortas;
        this.cor = cor;
    }
}
